    private void BotonRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistrarseActionPerformed
        if(Validar() == true){
            if(JOptionPane.showInternalConfirmDialog(null, "¿Confirmar Registro?") == 0){
                try {
                    Statement st;
                    ResultSet rs;

                    String nombreU = LeerNombreDeUsuario.getText();
                    String nombresU = LeerNombres.getText();
                    String apellidosU = LeerApellidos.getText();
                    Integer edadU = Integer.valueOf(LeerEdad.getText());
                    String direccionU = LeerDireccion.getText();
                    String tipoU = LeerTipoDeUsuario.getSelectedItem().toString();

                    String passU = "";
                    char []password = LeerContraseña.getPassword();
                    int tamanio = password.length;
                    for(int x = 0; x<tamanio; x++){
                        passU+=password[x];
                    }

                    boolean bandera = false;//Para verificar que los datos no sean repetidos
                    st = conexion.cx.createStatement();
                    rs = st.executeQuery("Select * From barberos");
                    Collator comparador = Collator.getInstance();
                    comparador.setStrength(Collator.PRIMARY);
                    String aux;
                    while (rs.next() && bandera == false){
                        aux =rs.getString("nombre_usuario");
                        if(comparador.equals(aux, nombreU)){
                            bandera = true;
                            JOptionPane.showMessageDialog(null,"Usuario: "+nombreU+" ya existe.");
                        }
                    }
                    if(bandera == false){//Para ingresar el nuevo usuario
                        PreparedStatement pps = conexion.cx.prepareStatement("INSERT INTO barberos(nombre_usuario,contraseña,nombres,apellidos,edad,direccion,tipo_u) VALUES(?,?,?,?,?,?,?)");
                        pps.setString(1,nombreU);
                        pps.setString(2, passU);
                        pps.setString(3, nombresU);
                        pps.setString(4, apellidosU);
                        pps.setInt(5, edadU);
                        pps.setString(6,direccionU);
                        pps.setString(7, tipoU);
                        pps.executeUpdate();
                        JOptionPane.showMessageDialog(null,"Registro Guardado.");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(InterfazRegistrarse.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_BotonRegistrarseActionPerformed

