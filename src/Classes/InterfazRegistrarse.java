    public boolean Validar(){
        int contV = 0;
                
        if(LeerNombreDeUsuario.getText().equals(""))
            CampoO1.setVisible(true);
        else{
            CampoO1.setVisible(false);
            contV+=1;
        }
        String passU = "";
        char []password = LeerContraseña.getPassword();
        int tamanio = password.length;
        for(int x = 0; x<tamanio; x++){
            passU+=password[x];
        }
        if(passU.equals(""))
            CampoO2.setVisible(true);
        else{
            CampoO2.setVisible(false);
            contV+=1;
        }
        if(LeerNombres.getText().equals(""))
            CampoO3.setVisible(true);
        else{
            CampoO3.setVisible(false);
            contV+=1;
        }
        if(LeerApellidos.getText().equals(""))
            CampoO4.setVisible(true);
        else{
            CampoO4.setVisible(false);
            contV+=1;
        }
        if(LeerEdad.getText().equals(""))
            CampoO5.setVisible(true);
        else{
            CampoO5.setVisible(false);
            contV+=1;
        }
        if(LeerDireccion.getText().equals(""))
            CampoO6.setVisible(true);
        else{
            CampoO6.setVisible(false);
            contV+=1;
        }
        if(LeerTipoDeUsuario.getSelectedItem().toString().equals("..."))
            CampoO7.setVisible(true);
        else{
            CampoO7.setVisible(false);
            contV+=1;
        }
        
        return contV == 7;
    }
    
