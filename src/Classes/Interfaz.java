    public boolean Validar(){
        int contV = 0;
                
        if(LeerUsuario.getText().equals(""))
            CampoO1.setVisible(true);
        else{
            CampoO1.setVisible(false);
            contV+=1;
        }
        String passU = "";
        char []password = LeerContraseña.getPassword();
        int tamanio = password.length;
        for(int x = 0; x<tamanio; x++){
            passU+=password[x];
        }
        if(passU.equals(""))
            CampoO2.setVisible(true);
        else{
            CampoO2.setVisible(false);
            contV+=1;
        }
        
        return contV == 2;
    }
    
