    private void BotonLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonLogInActionPerformed
        if(Validar() == true){
            
            try {
                Statement st;
                ResultSet rs;
                
                int id;
                String nombreU = LeerUsuario.getText();
                String passU = "";
                char []password = LeerContraseña.getPassword();
                int tamanio = password.length;
                for(int x = 0; x<tamanio; x++){
                    passU+=password[x];
                }
                
                boolean bandera = false;//Para verificar que existe un usuario con ese nombre
                st = conexion.cx.createStatement();
                rs = st.executeQuery("Select * From barberos");
                
                Collator comparador = Collator.getInstance();
                comparador.setStrength(Collator.PRIMARY);
                while (bandera == false && rs.next()){
                    id = rs.getInt("id_barbero");
                    if(comparador.equals(rs.getString("nombre_usuario"),nombreU)){
                        bandera = true;
                        if(comparador.equals(rs.getString("contraseña"),passU)){
                            if(rs.getString("tipo_u").equals("Administrador")){
                                conexion.Desconectar();
                                InterfazAdmin vIA = new InterfazAdmin();
                                vIA.setVisible(true);
                                this.setVisible(false);
                            }else{
                                conexion.Desconectar();
                                InterfazBarbero vIB = new InterfazBarbero();
                                vIB.setBarbero(id, nombreU);
                                vIB.setVisible(true);
                                this.setVisible(false);
                            }
                        }else
                            JOptionPane.showMessageDialog(null,"Contraseña Incorrecta");
                        break;
                    }
                }
                if(bandera == false)
                    JOptionPane.showMessageDialog(null,"Usuario: "+nombreU+" INEXISTENTE");
            } catch (SQLException ex) {
                Logger.getLogger(InterfazRegistrarse.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_BotonLogInActionPerformed

